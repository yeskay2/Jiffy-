    <script>
        const texts = ["Login as Management","Login as Human Resource","Login as Business / Sales", "Login as Project Management" , "Login as Finance" , ];
        const typewriterElement = document.getElementById("typewriter");
        let textIndex = 0;
        let charIndex = 0;

        function typeWriter() {
            if (textIndex < texts.length) {
                if (charIndex < texts[textIndex].length) {
                    typewriterElement.innerHTML += texts[textIndex].charAt(charIndex);
                    charIndex++;
                    setTimeout(typeWriter, 100);
                } else {
                    setTimeout(hideText, 100);
                }
            }
        }

        function hideText() {
            if (typewriterElement.innerHTML.length > 0) {
                typewriterElement.innerHTML = typewriterElement.innerHTML.slice(0, -1);
                setTimeout(hideText, 50);
            } else {
                textIndex = (textIndex + 1) % texts.length;
                charIndex = 0;
                setTimeout(typeWriter, 100);
            }
        }

        typeWriter();
    </script>

